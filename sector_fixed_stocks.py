def is_kcb_cyb(code):
    """Check if stock is 科创板(688xxx) or 创业板(300xxx/301xxx)"""
    return code.startswith('688') or code.startswith('300') or code.startswith('301')

def is_mainboard(code):
    """Check if stock is 主板 (600/601/603/605/000/001/002/003)"""
    return code.startswith('600') or code.startswith('601') or code.startswith('603') or code.startswith('605') or \
           code.startswith('000') or code.startswith('001') or code.startswith('002') or code.startswith('003')

def validate_stocks(stocks, sector_name=""):
    """Validate that stocks respect the 科创/创业板 ≤3 rule."""
    kcb_cyb = [s for s in stocks if is_kcb_cyb(s.split()[0])]
    main = [s for s in stocks if is_mainboard(s.split()[0])]
    other = [s for s in stocks if not is_kcb_cyb(s.split()[0]) and not is_mainboard(s.split()[0])]
    errors = []
    if len(kcb_cyb) > 3:
        errors.append(f"[{sector_name}] 科创/创业板 {len(kcb_cyb)} 只 (≤3)")
    if other:
        errors.append(f"[{sector_name}] 非主板非科创 {other}")
    if errors:
        for e in errors:
            print(f"  ⚠️ {e}")
        return False
    return True

